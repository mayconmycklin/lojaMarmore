    private void bpnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bpnConfirmarActionPerformed
            if(txtNome.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "digite seu nome completo");
            txtNome.requestFocus();
                      
        }
        else if (txtEmail.getText().isEmpty())
        {
            
            JOptionPane.showMessageDialog(null, "digite seu e-mail");
            txtEmail.requestFocus();   
        } 
            else if (txtCelular.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "digite seu numero telefonico");
            txtCelular.requestFocus(); 
            }        
       else if (txtCpf.getText().isEmpty())
        {
            
            JOptionPane.showMessageDialog(null, "digite seu CPF");
            txtCpf.requestFocus();   
        } 
            else if (txtEndereco.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "digite seu endereço");
            txtEndereco.requestFocus(); 
            }   
            else if (txtCep.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "digite o CEP do seu endereço");
            txtCep.requestFocus(); 
            }   
            else
            {  
        JOptionPane.showMessageDialog(rootPane, "Cadastro confirmado com sucesso!");
            
            TelaVendaMarmore abrirloja = new TelaVendaMarmore ();
        abrirloja.setVisible(true);
        dispose();
            }
    }//GEN-LAST:event_bpnConfirmarActionPerformed

